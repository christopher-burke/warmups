/*
Return the First Element in an Array.

Create a
function that takes an array and returns the first element.

Source:
https: //edabit.com/challenge/QaApgtePE6QrCZ64o
*/

function getFirstValue(arr) {
    return arr[0];
}

/**
 * Run sample getFirstValue functions, print results to console.log.
 */
console.log(getFirstValue([1, 2, 3]))
console.log(getFirstValue([80, 5, 100]))
console.log(getFirstValue([-500, 0, 50]))
console.log(getFirstValue([5, 2, 3]))
console.log(getFirstValue([75675, 5, 100]))
console.log(getFirstValue([-52320, 0, 50]))